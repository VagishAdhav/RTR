#include <stdio.h>

int main(void)
{
    // variable declaration 
    int vva_i;

    //code
    printf("\n\n");

    printf("Printing digits from 10 to 1 : \n\n");

    for (vva_i = 10; vva_i >= 1; vva_i--)
    {
        printf("\t%d\n", vva_i);
    }

    printf("\n\n");

    return (0);
}