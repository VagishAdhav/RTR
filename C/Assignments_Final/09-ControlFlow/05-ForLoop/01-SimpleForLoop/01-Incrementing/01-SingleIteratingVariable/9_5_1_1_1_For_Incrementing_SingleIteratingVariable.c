#include <stdio.h>

int main(void)
{
    //variable declaration
    int i_vva;

    //code

    printf("\n\n");

    printf("Printing digits 1 to 10 : \n\n");

    for (i_vva = 1; i_vva <= 10; i_vva++)
    {
        printf("\t%d\n", i_vva);
    }

    printf("\n\n");

    return (0);
}